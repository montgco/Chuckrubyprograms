DAYS_IN_YEAR = 365
HOURS_IN_DAY = 24
MIN_IN_HOUR = 60

def calc_min_in_year

  total_min_in_year = (DAYS_IN_YEAR * HOURS_IN_DAY) * MIN_IN_HOUR

end

puts "Number of minutes in a year: #{total_min_in_year = calc_min_in_year}"
